{
    use Queueable, SerializesModels;

    public $user;

    public function __construct($user)
    {
        $this->user = $user;
    }

    public function build()
    {
        return $this->view('emails.welcome') // VÃ©rifie que Ã§a correspond bien au fichier dans "views/emails/"
                    ->with(['user' => $this->user]);
    }
}
